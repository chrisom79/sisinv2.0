package com.chrisom.sisinv.utils;

public class WaayException extends Exception {

	public WaayException() {
		super();
	}

	public WaayException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public WaayException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WaayException(String arg0) {
		super(arg0);
	}

	public WaayException(Throwable arg0) {
		super(arg0);
	}
	
}
